void	ft_striteri(char *str, void (*f)(unsigned int, char *))
{
	unsigned int i;

	if (!str || !f)
		return ;
	i = 0;
	while (str[i] != '\0')
	{
		f(i, &str[i]);
		i++;
	}
}
